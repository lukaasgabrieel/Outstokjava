    public void preencherTabela(String SQL) {
        ArrayList dados = new ArrayList();

        String[] Colunas = new String[]{"ID", "Nome", "Telefone", " Celular", "Endereço",};

        conecta.executaSQL(SQL);
        try {
            conecta.rs.first();
            do {
                dados.add(new Object[]{
                    conecta.rs.getInt("id"),
                    conecta.rs.getString("nome"),
                    conecta.rs.getString("telefone"),
                    conecta.rs.getString("celular"),
                    conecta.rs.getString("endereco"),});
            } while (conecta.rs.next());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro preencher a tabela" + ex);
        }
        ModeloTabela modelo = new ModeloTabela(dados, Colunas);
        tabelaestado1.setModel(modelo);
        tabelaestado1.setAutoResizeMode(tabelaestado1.AUTO_RESIZE_ALL_COLUMNS);
        tabelaestado1.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
    }

