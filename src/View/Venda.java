    public void preencherTabela3(String SQL) {
        ArrayList dados = new ArrayList();

        String[] Colunas = new String[]{"Nome", "Preço", " Quantidade", "Total"};

        conecta.executaSQL(SQL);
        try {
            conecta.rs.first();

            do {
                int vendaQtd = conecta.rs.getInt("produtos_has_venda.quantidade");
                float valPro = conecta.rs.getFloat("produtos.preco_venda");
                dados.add(new Object[]{
                    conecta.rs.getString("produtos.nome"),
                    conecta.rs.getString("produtos.preco_venda"),
                    conecta.rs.getString("produtos_has_venda.quantidade"),
                    valPro * vendaQtd});
            } while (conecta.rs.next());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro preencher a tabela" + ex);
        }
        ModeloTabela modelo = new ModeloTabela(dados, Colunas);
        tabelaestado.setModel(modelo);
        tabelaestado.setAutoResizeMode(tabelaestado.AUTO_RESIZE_ALL_COLUMNS);
        tabelaestado.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        somaProduto();

    }

    public void somaProduto() {
        float total = 0;
        conecta.executaSQL("SELECT * FROM `produtos_has_venda`,produtos, venda WHERE `produtos_id`=produtos.id and venda_id = venda.id and venda.id='" + codVenda + "'");
        try {
            while (conecta.rs.next()) {
                int qtd = conecta.rs.getInt("produtos_has_venda.quantidade");
                float preco = conecta.rs.getFloat("produtos.preco_venda");
                total = total + qtd * preco;
            }
            SubTotal.setText(String.valueOf(total));

        } catch (SQLException ex) {
            Logger.getLogger(Venda.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

